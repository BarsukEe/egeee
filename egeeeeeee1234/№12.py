    s=list(' '+s+' ') 
    q=0 
    i=0
    while True:
        cmd=command[(s[i],q)] 
        s[i]=cmd[0]
        if cmd[1]==3: break
        i+=cmd[1]
        q=cmd[2]
    return ''.join(s)
